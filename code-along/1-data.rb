# In Ruby, there are different *types* of data:

# Numbers
puts 1
puts 5

# Non-numbers
puts 2 + 5
puts 2 - 5

# True, False
puts "Hello, World"
# Nothing
puts "tacos" * 3
# Variables
x = 2
y = 5
puts x 

# Combine strings and variables
first_name = "Mando"
greeting = "Hello, #{first_name}!"
puts greeting

puts "Hello      " + "there!"
puts "Hello".length
puts "how are you today?".capitalize
puts "computer".reverse

creed = "This Is The Way"
puts creed.upcase
puts creed.downcase
puts creed.swapcase

